import { useEffect } from 'react';
import { useParams, Link, useLocation } from 'react-router-dom';
import { Articles } from '../../../data/articles';
import { ArticleCard } from '../ArticleCard/ArticleCard';
import s from './ArticlePage.module.css';

export const ArticlePage = () => {
  const { id } = useParams<{ id: string }>();
  const { pathname } = useLocation();

  useEffect(() => {
    window.scrollTo(0, 0);
  }, [pathname]);

  const currentArticle = Articles.find(art => String(art.id) === id);

  if (!currentArticle) {
    return <div className="container"><h2>Статья не найдена</h2></div>;
  }

  const recommendedArticles = Articles
    .filter(art => art.topic === currentArticle.topic && String(art.id) !== id)
    .slice(0, 4); // На макете в ряду 3 карточки

  return (
    <div className={s.page_container}>
      <Link to="/articles" className={s.back_link}>
        &lt; Назад к статьям
      </Link>
      <article className={s.article}>
        <p className={s.paragraph}>Как известно, в нашей климатической зоне строительный сезон традиционно начинался весной. Однако в последние годы с появлением в современном зодчестве высокотехнологичных «ноу-хау» здания и сооружения растут практически круглый год, что многократно увеличивает возможности строительного рынка. В результате невзирая на всевозможные кризисы, отрасль переживает поистине революционный бум. Особенно это касается крупных российских индустриально-финансовых центров (Волгоград, Пермь, Сочи, Тюмень, Уфа, Казань) и мегаполисов (Москва, Питер). Из-за сравнительно высокого уровня жизни населения все больший размах здесь приобретает частное (или как было принято говорить в советский период – индивидуальное) строительство. Во что сегодня обходятся нашим соотечественникам строительно-монтажные операции, хорошо осведомлен персонал фирм, специализирующихся на предоставлении профильных услуг.
        В данном материале использована информация, взятая из вышеупомянутого источника.</p>
        
        <h2 className={s.subtitle}>Руководствуйтесь проектом, а не прожектом</h2>
        <p className={s.paragraph}>В настоящее время при возведении дома без квалифицированного проекта не обойтись. Лучше всего, если его в индивидуальном порядке выполнит архитектор. Правда, в этом случае придется раскошелиться примерно на $900. Типовой проект у застройщика будет стоить почти вполовину дешевле… Так или иначе, строить свое детище на основании сомнительных доморощенных схем специалисты не рекомендуют. А геологическая экспертиза под строительство коттеджа, которая необязательна, хотя и желательна (особенно в отдельных случаях), с учетом проведения буровых работ обойдется на порядок дороже проекта.</p>
        
        <h2 className={s.subtitle}>Не стройте замок на песке</h2>
        <p className={s.paragraph}>Как известно, грунт в Средней полосе довольно зыбкий. Несмотря на данное обстоятельство кое-кто из несведущих или нерадивых хозяев роют для фундамента мелкую траншею, вымащивают ее бетоноблоками, бутят оставшиеся зазоры гравием или щебнем, а затем все заливают бетоном. А между тем из элементарных инженерных соображений делать это категорически воспрещается! Необходимо обязательно предусмотреть изоляционную конструкцию, например, опалубку. Ведь почва в виде раствора впитывает жидкую субстанцию, которая при застывании придает фундаменту твердость. Вдобавок глубина заложения оказывается выше уровня промерзания, а это отражается на морозоустойчивости материала. Именно поэтому траншею (котлован) копают минимум на 1,30 м.</p>
      </article>

      {recommendedArticles.length > 0 && (
        <section className={s.recommendations}>
          <h2 className={s.rec_main_title}>Другие статьи темы</h2>
          <div className={s.grid}>
            {recommendedArticles.map(art => (
              <ArticleCard key={art.id} article={art} />
            ))}
          </div>
        </section>
      )}
    </div>
  );
};
